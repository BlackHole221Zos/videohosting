import os
from flask import current_app
from fpdf import FPDF


def generate_legal_pdfs():
    """Генерирует PDF для Terms и Privacy"""

    docs_folder = os.path.join(current_app.static_folder, 'docs')
    os.makedirs(docs_folder, exist_ok=True)

    terms_path = os.path.join(docs_folder, 'terms.pdf')
    privacy_path = os.path.join(docs_folder, 'privacy.pdf')

    if not os.path.exists(terms_path):
        print('📄 Генерация terms.pdf...')
        _generate_terms(terms_path)
        print('✅ terms.pdf создан')

    if not os.path.exists(privacy_path):
        print('📄 Генерация privacy.pdf...')
        _generate_privacy(privacy_path)
        print('✅ privacy.pdf создан')


class VidSpherePDF(FPDF):
    """PDF с русским текстом через DejaVu"""

    def __init__(self):
        super().__init__()
        # Добавляем шрифт DejaVu (поддерживает кириллицу)
        self.add_font('DejaVu', '', 'DejaVuSans.ttf')
        self.add_font('DejaVu', 'B', 'DejaVuSans-Bold.ttf')
        self.add_font('DejaVu', 'I', 'DejaVuSans-Oblique.ttf')

        self.set_auto_page_break(auto=True, margin=25)

    def header(self):
        self.set_font('DejaVu', 'I', 8)
        self.set_text_color(150, 150, 150)
        self.cell(0, 10, 'VidSphere', align='L')
        self.ln(5)
        self.set_draw_color(78, 205, 196)
        self.set_line_width(0.5)
        self.line(10, self.get_y(), 200, self.get_y())
        self.ln(5)

    def footer(self):
        self.set_y(-15)
        self.set_font('DejaVu', 'I', 8)
        self.set_text_color(150, 150, 150)
        self.cell(0, 10, f'VidSphere © 2026  |  Стр. {self.page_no()}/{{nb}}', align='C')

    def add_title(self, text):
        self.set_font('DejaVu', 'B', 20)
        self.set_text_color(0, 0, 0)
        self.cell(0, 15, text, align='C', new_x='LMARGIN', new_y='NEXT')
        self.ln(3)

    def add_subtitle(self, text):
        self.set_font('DejaVu', '', 10)
        self.set_text_color(78, 205, 196)
        self.cell(0, 8, text, align='C', new_x='LMARGIN', new_y='NEXT')
        self.ln(2)

    def add_date(self, text):
        self.set_font('DejaVu', 'I', 9)
        self.set_text_color(130, 130, 130)
        self.cell(0, 8, text, align='C', new_x='LMARGIN', new_y='NEXT')
        self.ln(8)

    def add_heading(self, text):
        self.ln(5)
        self.set_font('DejaVu', 'B', 13)
        self.set_text_color(0, 0, 0)
        self.cell(0, 10, text, new_x='LMARGIN', new_y='NEXT')
        self.set_draw_color(78, 205, 196)
        self.set_line_width(0.8)
        self.line(10, self.get_y(), 100, self.get_y())
        self.ln(5)

    def add_paragraph(self, text):
        self.set_font('DejaVu', '', 10)
        self.set_text_color(30, 30, 30)
        self.multi_cell(0, 6, text)
        self.ln(3)

    def add_bullet(self, text):
        self.set_font('DejaVu', '', 10)
        self.set_text_color(30, 30, 30)
        x = self.get_x()
        self.cell(8, 6, '•')
        self.multi_cell(0, 6, text)
        self.ln(1)


